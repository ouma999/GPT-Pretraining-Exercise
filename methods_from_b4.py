# the data loader will call the data set class GPT Dataset Version 1
#  to perform the dataset half of the process. The definition here
#  is for creation of the data loader to get the text in place
def create_dataloader_v1(txt, batch_size=4, max_length=1024, 
                         stride=128, shuffle=True, drop_last=True,
                         num_workers=0):

    # Initialize the tokenizer
    tokenizer = tiktoken.get_encoding("gpt2")

    # Create dataset
    dataset = GPTDatasetV1.GPTDatasetV1(txt, tokenizer, max_length, stride)

    # Create dataloader
    dataloader = DataLoader(
        dataset,
        batch_size=batch_size,
        shuffle=shuffle,
        drop_last=drop_last,
        num_workers=num_workers
    )

    return dataloader

